#include "Aiquam.hpp"

Aiquam::~Aiquam() = default;

Aiquam::Aiquam(std::shared_ptr<Config> config): config(config) {
    logger = log4cplus::Logger::getInstance(LOG4CPLUS_TEXT("Aiquam"));

    session_options.SetIntraOpNumThreads(1);
    session_options.SetGraphOptimizationLevel(GraphOptimizationLevel::ORT_ENABLE_EXTENDED);
}

// Define a template function to process the output tensor based on its type
template <typename T>
void Aiquam::processOutputTensor(Ort::Value& output_tensor, std::vector<int64_t>& predictions, size_t input_data_size) {
    if constexpr (std::is_integral<T>::value) {
        T* output_data = output_tensor.GetTensorMutableData<T>();
        predictions.push_back(static_cast<int64_t>(output_data[0]));
    } else {
        T* float_array = output_tensor.GetTensorMutableData<T>();
        std::vector<T> output_vector(float_array, float_array + input_data_size);
        auto max_element_iter = std::max_element(output_vector.begin(), output_vector.end());
        int64_t predicted_class = std::distance(output_vector.begin(), max_element_iter);
        predictions.push_back(predicted_class);
    }
}

// Function to determine the type and call the appropriate processing function
void Aiquam::getClass(Ort::Value& output_tensor, const std::string& output_type, std::vector<int64_t>& predictions, size_t input_data_size) {
    if (output_type == "int") {
        processOutputTensor<int>(output_tensor, predictions, input_data_size);
    } else if (output_type == "float") {
        processOutputTensor<float>(output_tensor, predictions, input_data_size);
    } else if (output_type == "int64_t") {
        processOutputTensor<int64_t>(output_tensor, predictions, input_data_size);
    } else {
        throw std::runtime_error("Unsupported output type");
    }
}

int Aiquam::majority_vote() {
    std::unordered_map<int, int> frequencyMap;

    // Count the occurrences of each element
    for (int num : predictions) {
        frequencyMap[num]++;
    }

    // Find the element with the highest frequency
    int mostFrequent = predictions[0];
    int maxCount = 0;

    for (const auto& pair : frequencyMap) {
        if (pair.second > maxCount) {
            maxCount = pair.second;
            mostFrequent = pair.first;
        }
    }

    return mostFrequent;
}

